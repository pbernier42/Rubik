#include <rubik.h>

void	display(char ***tab, int face, int cel[12])
{
	int		i;
	int		j;

	i = 0;
	cel = (int[12]){7, 4, 7, 7, 3, 7, 5, 1, 2, 7, 6, 7};
	(void)face;
	while (i < 12)
	{
		j = -1;
		while (++j < 9)
		{
			if (face == -1 && cel[i + (j % 3)] == 7)
				ft_putstr("   ");
			else
				ft_putstr(tab[6][(cel[i + (j % 3)] * 3) - 2]);
			if (j % 3 == 2)
				ft_putchar('\n');
		}
		i += 3;
	}
}
